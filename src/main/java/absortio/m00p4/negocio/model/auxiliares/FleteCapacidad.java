package absortio.m00p4.negocio.model.auxiliares;

public class FleteCapacidad {

    private Integer n;
    private Double pesomin;
    private  Double pesomax;
    private String unidpeso;
    private  Double volmin;
    private Double volmax;
    private String unidvol;
    private Double costo;

    public Integer getN() {
        return n;
    }

    public void setN(Integer n) {
        this.n = n;
    }

    public Double getPesomin() {
        return pesomin;
    }

    public void setPesomin(Double pesomin) {
        this.pesomin = pesomin;
    }

    public Double getPesomax() {
        return pesomax;
    }

    public void setPesomax(Double pesomax) {
        this.pesomax = pesomax;
    }

    public String getUnidpeso() {
        return unidpeso;
    }

    public void setUnidpeso(String unidpeso) {
        this.unidpeso = unidpeso;
    }

    public Double getVolmin() {
        return volmin;
    }

    public void setVolmin(Double volmin) {
        this.volmin = volmin;
    }

    public Double getVolmax() {
        return volmax;
    }

    public void setVolmax(Double volmax) {
        this.volmax = volmax;
    }

    public String getUnidvol() {
        return unidvol;
    }

    public void setUnidvol(String unidvol) {
        this.unidvol = unidvol;
    }

    public Double getCosto() {
        return costo;
    }

    public void setCosto(Double costo) {
        this.costo = costo;
    }
}
